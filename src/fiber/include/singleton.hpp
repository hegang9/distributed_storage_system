// Monsoon 框架的单例模式实现头文件
// 提供两种单例模式封装：
// 1. Singleton<T>：返回裸指针的单例（轻量级，无引用计数）
// 2. SingletonPtr<T>：返回智能指针的单例（带引用计数，支持自动内存管理）
//
// 核心思想：利用 C++ 静态变量的初始化特性
// - 静态局部变量在首次访问时初始化一次（线程安全，C++11 及以后）
// - 静态全局变量在程序启动时初始化
// - 通过静态存储时长保证整个程序生命周期内只存在一个实例

#ifndef __MONSOON_SINGLETON_H__
#define __MONSOON_SINGLETON_H__

#include <memory>

namespace monsoon {
// 匿名命名空间：包含两个模板函数，用于内部实现。它们展示了另一种实现单例的方式（通过显式的模板参数指定）。使用匿名命名空间第一可以解决命名冲突，第二可以隐藏实现细节，防止外部访问（匿名命名空间中的符号只能在当前文件中访问，外部无法访问）。
namespace {
// 获取单例对象的引用形式
// 模板参数说明：
//   T：单例对象的类型
//   X：标记类型，用来区分不同的单例配置（允许同一类型创建多个逻辑单例）
//   N：索引编号，同一个 X 标记下可创建多个实例
// 返回值：T 类型的引用，指向静态存储区的唯一实例
// 关键：static T v; 保证该变量在整个程序生命周期内只初始化一次
template <class T, class X, int N>
T &GetInstanceX() {
  static T v;  // 静态局部变量，首次调用时初始化，之后每次调用返回同一对象
  return v;
}

// 获取单例对象的智能指针形式
// 模板参数同 GetInstanceX
// 返回值：std::shared_ptr<T>，包含唯一实例的智能指针
// 优点：支持引用计数和自动内存管理
// 缺点：相比裸指针有额外的引用计数开销
template <class T, class X, int N>
std::shared_ptr<T> GetInstancePtr() {
  static std::shared_ptr<T> v(new T);  // 智能指针的静态变量，全程生命周期内维护一个引用
  return v;
}
}  // namespace

// ============================================================================
// 单例模式实现 - 裸指针版本
// ============================================================================
// @brief 轻量级单例模式封装类，返回裸指针
//
// @tparam T 单例对象的实际类型
//          示例：Singleton<Logger> 创建 Logger 的单例
//
// @tparam X 标记/Tag 类型，用于区分不同的单例配置
//          - 默认值：void（最常见用法）
//          - 用途：允许同一类型 T 在不同的 X Tag 下创建不同的逻辑单例
//          - 示例：Singleton<Logger, TagA> 和 Singleton<Logger, TagB> 是独立的单例
//
// @tparam N 索引编号，同一个 X Tag 下可创建多个实例的索引
//          - 默认值：0
//          - 用途：Singleton<T, X, 0>、Singleton<T, X, 1> 等分别对应不同的实例
//          - 实际应用场景较少，通常只使用默认的 N=0
//
// @note 线程安全性：
//       - C++11 标准规定，函数内的静态变量初始化是线程安全的（magic statics）
//       - 首次调用 GetInstance() 时，多个线程不会重复初始化对象
//       - 初始化完成后，所有后续调用都返回同一对象
//
// @note 适用场景：
//       - 配置管理器（全局唯一的配置对象）
//       - 日志系统（全局唯一的日志对象）
//       - 轻量级对象，不需要复杂的内存管理
/**
 * @brief 单例模式封装类
 * @details T 类型
 *          X 为了创造多个实例对应的Tag
 *          N 同一个Tag创造多个实例索引
 */
template <class T, class X = void, int N = 0>
class Singleton {
 public:
  /**
   * @brief 返回单例裸指针
   */
  static T *GetInstance() { return &GetInstanceX<T, X, N>(); }
};

// ============================================================================
// 单例模式实现 - 智能指针版本
// ============================================================================
// @brief 智能指针版本的单例模式封装类
//
// @tparam T 单例对象的实际类型
//
// @tparam X 标记/Tag 类型
//          - 同 Singleton<T,X,N>，用于区分不同的单例配置
//
// @tparam N 索引编号
//          - 同 Singleton<T,X,N>，允许同一 X Tag 下有多个实例
//
// @note 与 Singleton<T> 的区别：
//       - Singleton<T>：返回裸指针，不涉及引用计数
//       - SingletonPtr<T>：返回 std::shared_ptr，支持引用计数
//       - 都是线程安全的（C++11 magic statics）
//       - SingletonPtr 适合需要复杂内存管理的场景
//
// @note 生命周期管理：
//       - std::shared_ptr<T> 会记录引用计数
//       - 只要还有地方持有该指针，对象就不会被删除
//       - 所有持有者都释放指针后，对象自动销毁
//       - 注意：单例模式下通常不需要手动释放，但智能指针提供了更灵活的控制
//
// @note 适用场景：
//       - 需要支持引用计数的单例（如资源管理器）
//       - 需要在运行时检查对象是否被销毁的场景
//       - 与需要 shared_ptr 接口的代码兼容
/**
 * @brief 单例模式智能指针封装类
 * @details T 类型
 *          X 为了创造多个实例对应的Tag
 *          N 同一个Tag创造多个实例索引
 */
template <class T, class X = void, int N = 0>
class SingletonPtr {
 public:
  // 返回单例智能指针
  static std::shared_ptr<T> GetInstance() { return GetInstancePtr<T, X, N>(); }
};

}  // namespace monsoon

// ============================================================================
// 总结：何时使用哪个单例模式
// ============================================================================
// 1. Singleton<T> - 裸指针版本
//    用途：简单的全局对象（配置、日志等轻量级场景）
//    优点：无引用计数开销，性能最优
//    缺点：无法灵活控制生命周期
//
// 2. SingletonPtr<T> - 智能指针版本
//    用途：需要引用计数的场景（资源管理、复杂对象等）
//    优点：支持引用计数，可跨函数传递，自动内存管理
//    缺点：多一层指针间接访问，引用计数有小额开销
//
// 两者都是线程安全的（C++11 编译器保证）
// ============================================================================

#endif